import UIKit

extension UIColor {
    static var detail: UIColor  { return UIColor(named: "Detail")! }
    static var secondary: UIColor  { return UIColor(named: "Secondary")! }
    static var selection: UIColor  { return UIColor(named: "Selection")! }
    static var background: UIColor  { return UIColor(named: "Background")! }
}
